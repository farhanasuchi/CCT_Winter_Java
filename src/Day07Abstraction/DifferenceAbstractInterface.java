package Day07Abstraction;

public class DifferenceAbstractInterface {
	
	
	
	/*
	 * 
	 * 
	 * 1.To create abstract class, we use 'abstract' keyword in front of the class name.
	 *     To create interface , we use 'interface' keyword.
	 * 
	 * 
	 * 
	 * 2.When a regular class wants to extend an abstract class, it uses 'extends' keyword. 
	 *      But interface , it uses 'implements' keyword.
	 * 
	 * 
	 * 
	 * 3.Abstract class allows both implemented and unimplemented methods.
	 *      but interface only allows unimplemented methods.
	 * 
	 * 
	 * 4.We can not achieve 100% abstraction by abstract classes. 
	 *         but by interface, we can achieve 100% abstraction.
	 * 
	 * 
	 * 5.Abstract class does not allow multiple  inheritance in java .
	 *       but, interface does allow.
	 * 
	 * 
	 * 6.Abstract class allow constructors.
	 *      but interface does not allow constructors.
	 * 
	 * 
	 * 
	 * 7. An abstract class can implement an interface.
	 *     but an interface can not extend an abstract class.
	 * 
	 * 
	 * 
	 * 8.After implementing an interface , a regular class can not extend an abstract class.
	 *        but after extending an abstract class, a regular class can implement an interface.
	 * 
	 * 
	 * 
	 * 
	 * 
	 */
	

}
